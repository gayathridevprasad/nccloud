    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
         try {

            System.out.println(fname);
            ServerSocket ss = new ServerSocket(500);
            Socket sock = ss.accept();
            PrintStream ps = new PrintStream(sock.getOutputStream());
            BufferedReader br = new BufferedReader(new InputStreamReader(sock.getInputStream()));

            int n = fname.size();
            ps.println(Integer.toString(n));

            for (int i = 0; i < n; i++) {
                ps.println(fname.get(i));
            }
            BufferedInputStream bis = new BufferedInputStream(sock.getInputStream());
            DataInputStream dis = new DataInputStream(bis);
            String b = br.readLine();
            long fileLength = dis.readLong();
            File fj = new File(dpath1 + "/" + b);
            FileOutputStream fos = new FileOutputStream(fj);
            BufferedOutputStream bos = new BufferedOutputStream(fos);

            for (int j = 0; j < fileLength; j++) {
                bos.write(bis.read());
            }

            bos.close();

            JOptionPane.showMessageDialog(this, b+" uploaded successfully");

            br.close();
            ps.close();
            sock.close();
            ss.close();

        } catch (Exception e) {
            System.out.println("Exception : " + e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

